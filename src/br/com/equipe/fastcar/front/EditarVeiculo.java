    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        VeiculoDAO veiculoDao = new VeiculoDAO();
        Veiculo veiculo = new Veiculo();
        veiculo = veiculoDao.findEspecific(txtPlacaPesquisa.getText());
        placaAux = txtPlacaPesquisa.getText();
        txtModelo.setText(veiculo.getModelo());
        txtChassi.setText(veiculo.getChassi());
        txtPlaca.setText(veiculo.getPlaca());
        txtKm.setText(String.valueOf(veiculo.getKilometragem()));
        txtCor.setText(veiculo.getCor());
        txtProprietario.setText(veiculo.getProprietario());
        txtMotor.setText(veiculo.getMotor());
        txtAno.setText(veiculo.getAno());
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        VeiculoDAO veiculoDao = new VeiculoDAO();
        Veiculo veiculo = new Veiculo();
        veiculo = veiculoDao.findEspecific(txtPlacaPesquisa.getText());
        

        //Pega as inforama√ß√µes digitadas
        veiculo.setProprietario(txtProprietario.getText());
        veiculo.setChassi(txtChassi.getText());
        veiculo.setPlaca(txtPlaca.getText());
        veiculo.setModelo(txtModelo.getText());
        veiculo.setKilometragem(Integer.valueOf(txtKm.getText()));
        veiculo.setMotor(txtMotor.getText());
        veiculo.setAno(txtAno.getText());


        veiculoDao.update(veiculo,placaAux);

        txtModelo.setText("");
        txtChassi.setText("");
        txtPlaca.setText("");
        txtKm.setText("");
        txtCor.setText("");
        txtProprietario.setText("");
        txtMotor.setText("");
        txtAno.setText("");
    }//GEN-LAST:event_btnSalvarActionPerformed

