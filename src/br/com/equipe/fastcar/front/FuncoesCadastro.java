    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        if (conta.getAcesso() == 1) {//ATENDENTE
            btnCadastroCliente.setEnabled(true);
            btnCadastroVeiculo.setEnabled(true);
            btnEditarCliente.setEnabled(true);
            btnEditaVeiculo.setEnabled(true);
        }
        if (conta.getAcesso() == 3) {//ESTOQUISTA
            btnCadastroProduto.setEnabled(true);
            btnEditarProduto.setEnabled(true);
        }
        if (conta.getAcesso() == 2) {//ESTOQUISTA
            btnCadastroProduto.setEnabled(true);
            btnEditarProduto.setEnabled(true);
            btnCadastroCliente.setEnabled(true);
            btnCadastroVeiculo.setEnabled(true);
            btnEditarCliente.setEnabled(true);
            btnEditaVeiculo.setEnabled(true);
        }
    }//GEN-LAST:event_formWindowOpened

