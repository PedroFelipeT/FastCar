    private void psSenhaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_psSenhaKeyReleased
        if (psSenha.getText().length() <= 4 && psSenha.getText().length() > 0) {
            btnCadastrar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "A senha deve conter no máximo 4 digitos!");
            psSenha.setText("");
            btnCadastrar.setEnabled(false);
        }

    }//GEN-LAST:event_psSenhaKeyReleased

    private void txtCpfKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCpfKeyReleased
        if (txtCpf.getText().equals("   .   .   -  ")) {
            JOptionPane.showMessageDialog(null, "Digite um CPF");
        } else {
            txtEndereco.setBackground(Color.white);
            txtEndereco.setEditable(true);

        }
    }//GEN-LAST:event_txtCpfKeyReleased

    private void txtEnderecoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEnderecoKeyReleased
        if (txtEndereco.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um Endreço");
        } else {
            txtCidade.setBackground(Color.white);
            txtCidade.setEditable(true);
        }
    }//GEN-LAST:event_txtEnderecoKeyReleased

