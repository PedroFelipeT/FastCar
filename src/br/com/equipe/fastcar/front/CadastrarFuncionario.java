    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // Salvar todos os dados do funcionario
        Departamento departamento = (Departamento) cbDep.getSelectedItem(); //Cast para converter o item selecionado

        Funcionario f = new Funcionario();
        Conta c = new Conta();

        FuncionarioDAO funDao = new FuncionarioDAO();
        ContaDAO conDao = new ContaDAO();

        if (!txtLogin.getText().equals("") && !psSenha.getText().equals("")) {
            //Pega as informações do funcionario nos campos de textos
            f.setNome(txtNome.getText());
            f.setCpf(txtCpf.getText());
            f.setEndereco(txtEndereco.getText());
            f.setCidade(txtCidade.getText());
            f.setTelefone(txtTelefone.getText());
            f.setEmail(txtEmail.getText());
            f.setCodDepto(departamento.getCodigo());
            c.setLogin(txtLogin.getText());
            c.setSenha(psSenha.getText());
            c.setAcesso(departamento.getCodigo());

            funDao.save(f);
            conDao.save(c);

            txtNome.setText("");
            txtCpf.setText("");
            txtEndereco.setText("");
            txtCidade.setText("");
            txtTelefone.setText("");
            txtEmail.setText("");
            txtLogin.setText("");
            psSenha.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os dados corretamente!");
        }


    }//GEN-LAST:event_btnCadastrarActionPerformed

