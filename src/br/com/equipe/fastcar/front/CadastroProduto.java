    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        PecaDAO pecaDao = new PecaDAO();
        if (pecaDao.checkRef(txtReferenciaBusca.getText())) {
            JOptionPane.showMessageDialog(null, "Produto já cadastrado!");
            btnVerificar.setIcon(new javax.swing.ImageIcon(getClass().getResource("/br/com/equipe/fastcar/icons/sair-menu-2.png")));
            List<Peca> testando = new ArrayList();
            testando = pecaDao.readForReference(txtReferenciaBusca.getText());

            if (txtReferenciaBusca.getText().equals("9999")) {
                JOptionPane.showMessageDialog(null, "Produtos imutáveis!");

            } else {
                for (Peca p : testando) {
                    txtNome.setText(p.getNome());
                    txtReferencia.setText(String.valueOf(p.getReferencia()));
                    txtFornecedor.setText(p.getFornecedor());
                    txtQuantidade.setText(String.valueOf(p.getQuantidade()));
                    txtValorCompra.setText(String.valueOf(p.getValorDeCompra()));;
                    txtValorVenda.setText(String.valueOf(p.getValorDeVenda()));;
                    
                    txtNome.setEditable(false);
                    txtReferencia.setEditable(false);
                    txtFornecedor.setEditable(false);
                    txtQuantidade.setEditable(false);
                    txtValorCompra.setEditable(true);
                    txtValorVenda.setEditable(true);
                }

                lblInserir.setVisible(true);
                txtInserir.setVisible(true);
                check = true;
            }

        } else {
            if (txtReferenciaBusca.getText().equals("0000")) {
                JOptionPane.showMessageDialog(null, "Digite uma referência valida!");

            } else {
                btnVerificar.setIcon(new javax.swing.ImageIcon(getClass().getResource("/br/com/equipe/fastcar/icons/Confirmaricon.png")));
                txtNome.setEditable(true);
                txtReferencia.setEditable(true);
                txtFornecedor.setEditable(true);
                txtQuantidade.setEditable(true);
                txtValorCompra.setEditable(true);
                txtValorVenda.setEditable(true);
                txtNome.setText("");
                txtReferencia.setText("");
                txtFornecedor.setText("");
                txtQuantidade.setText("");
                txtValorCompra.setText("");
                txtValorVenda.setText("");
                txtInserir.setVisible(false);
                lblInserir.setVisible(false);
            }
        }

    }//GEN-LAST:event_btnVerificarActionPerformed

