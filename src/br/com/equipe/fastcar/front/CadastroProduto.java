    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Salvar um poduto
        PecaDAO pecaDao = new PecaDAO();
        Peca peca = new Peca();
        if (check == true) {
            pecaDao.updateQuantidadeEntrada(Integer.valueOf(txtReferenciaBusca.getText()), Integer.valueOf(txtInserir.getText()));
            JOptionPane.showMessageDialog(null, "Quantidade atualizada com sucesso!");
        } else {
            //Pega as inforamações digitadas
            peca.setNome(txtNome.getText());
            peca.setReferencia(Integer.parseInt(txtReferencia.getText()));
            peca.setFornecedor(txtFornecedor.getText());
            peca.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
            peca.setValorDeCompra(Double.parseDouble(txtValorCompra.getText()));
            peca.setValorDeVenda(Double.parseDouble(txtValorVenda.getText()));

            pecaDao.save(peca);
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
        }

        txtNome.setText("");
        txtReferencia.setText("");
        txtFornecedor.setText("");
        txtQuantidade.setText("");
        txtValorCompra.setText("");
        txtValorVenda.setText("");
        txtInserir.setVisible(false);
        lblInserir.setVisible(false);
        txtInserir.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

